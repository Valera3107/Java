package module_1_4;

public class ArithmeticOperationsApp {
  public static void main(String[] args) {
    double d1 = 24;
    double d2 = 249;

    System.out.println("d1 / d2 = " + (d1/d2));
    System.out.println("d1 + d2 = " + (d1+d2));
    System.out.println("d1 - d2 = " + (d1-d2));
    System.out.println("d1 % d2 = " + (d1%d2));
    System.out.println("d1 * d2 = " + (d1*d2));
  }
}
