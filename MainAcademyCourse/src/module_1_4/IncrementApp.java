package module_1_4;

public class IncrementApp {
  public static void main(String[] args) {
    int a = 10;
    System.out.println("++a " + ++a);
    a = 10;
    System.out.println("a++ " + a++);
    a = 10;
    System.out.println("--a " + --a);
    a = 10;
    System.out.println("a-- " + a--);
  }
}
