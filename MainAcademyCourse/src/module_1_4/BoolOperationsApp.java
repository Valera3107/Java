package module_1_4;

public class BoolOperationsApp {
  public static void main(String[] args) {
    boolean b1 = true;
    boolean b2 = false;
    System.out.println(" (b1&&b2) logic \"and\": " + (b1&&b2));
    System.out.println(" (b1||b2) logic \"or\": " + (b1||b2));
    System.out.println(" (!b1) logic \"not\": " + (!b1));
    System.out.println(" (b1^b2) logic \"xor\": " + (b1^b2));
  }
}
